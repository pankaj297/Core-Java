package Introduction;

public class VariableDemo2 {
    public static void main(String[] args) {

        char ch1 = 'A';
        System.out.println("Char value stored is "+ ch1);

        char ch2 = 66;
        System.out.println("Char value stored is "+ ch2);

        boolean value = false;
        System.out.println("Boolean value stored is "+ value);
    }
}