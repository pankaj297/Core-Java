
package Operators.arithmetic;

public class ArithmeticOperations3 {
    public static void main(String[] args) {

        System.out.println(10 + 20 - 9 / 2); // 26
        System.out.println(10 * 2 / 9 / 2); //1
        System.out.println(10 * (2 / 9) / 2); // 0
        System.out.println(10 * (2 % 9) / 2); //10
    }
}