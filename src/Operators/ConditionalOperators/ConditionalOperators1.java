
package Operators.ConditionalOperators;

public class ConditionalOperators1 {
    public static void main(String[] args) {

        System.out.println(4 < 3 ? "4 " : "3 "); // 3
        System.out.println(4 % 2 == 0 ? "Even" : "Odd"); // Even
        System.out.println(3 % 2 == 0 ? "Even" : "Odd");// odd
    }
}